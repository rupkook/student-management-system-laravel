    <script>
        // Enhanced loading text animation
        const loadingTexts = [
            'Initializing AI-powered system...',
            'Loading neural networks...',
            'Preparing smart dashboard...',
            'Analyzing student data...',
            'Almost ready...',
            'Welcome to the future!'
        ];
        
        let textIndex = 0;
        const loadingTextElement = document.getElementById('loading-text');
        
        const updateLoadingText = () => {
            loadingTextElement.textContent = loadingTexts[textIndex];
            textIndex = (textIndex + 1) % loadingTexts.length;
        };
        
        // Update loading text every 600ms
        const textInterval = setInterval(updateLoadingText, 600);
        
        // Particle System
        const createParticle = () => {
            const particle = document.createElement('div');
            particle.className = 'particle';
            particle.style.left = Math.random() * window.innerWidth + 'px';
            particle.style.top = Math.random() * window.innerHeight + 'px';
            particle.style.width = Math.random() * 3 + 1 + 'px';
            particle.style.height = particle.style.width;
            particle.style.background = `rgba(255, 255, 255, ${Math.random() * 0.6 + 0.1})`;
            particle.style.borderRadius = '50%';
            particle.style.animationDelay = Math.random() * 10 + 's';
            particle.style.animationDuration = (Math.random() * 10 + 10) + 's';
            
            document.getElementById('particles').appendChild(particle);
            
            // Remove particle after animation
            setTimeout(() => {
                particle.remove();
            }, 20000);
        };
        
        // Create particles continuously
        const particleInterval = setInterval(createParticle, 300);
        
        // Skip intro function
        const skipIntro = () => {
            clearInterval(textInterval);
            clearInterval(particleInterval);
            loadingTextElement.textContent = 'Entering dashboard...';
            
            // Add fade out effect for all elements together
            const mainContent = document.querySelector('.text-center');
            mainContent.style.transition = 'opacity 1s ease-out';
            mainContent.style.opacity = '0';
            
            // Redirect to dashboard after fade
            setTimeout(() => {
                window.location.href = '/dashboard';
            }, 1000);
        };
        
        // Auto-redirect after 3 seconds
        setTimeout(() => {
            skipIntro();
        }, 3000);
        
        // Interactive effects
        document.addEventListener('DOMContentLoaded', () => {
            // Add hover effect to feature cards
            const features = document.querySelectorAll('.feature-card');
            features.forEach((feature, index) => {
                feature.style.animationDelay = `${1.2 + (index * 0.2)}s`;
            });
            
            // Add mouse move effect for parallax
            document.addEventListener('mousemove', (e) => {
                const x = e.clientX / window.innerWidth;
                const y = e.clientY / window.innerHeight;
                
                const logo = document.querySelector('.animate-float');
                if (logo) {
                    logo.style.transform = `translateY(-20px) rotateX(${(y - 0.5) * 10}deg) rotateY(${(x - 0.5) * 10}deg)`;
                }
            });
            
            // Add keyboard shortcut
            document.addEventListener('keydown', (e) => {
                if (e.key === 'Enter' || e.key === ' ') {
                    e.preventDefault();
                    skipIntro();
                }
            });
            
            // Create initial particles
            for (let i = 0; i < 20; i++) {
                setTimeout(createParticle, i * 100);
            }
        });
    </script>
